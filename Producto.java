package poo;

public class Producto {
	
	String producto1, producto2, producto3;
	double precio1, precio2, precio3;
	int cantidad1, cantidad2, cantidad3;
	
	Producto(String producto1,String producto2,String producto3,double precio1, 
			double precio2, double precio3, int cantidad1, int cantidad2, int cantidad3){
		this.producto1=producto1;
		this.producto2=producto2;
		this.producto3=producto3;
		this.precio1=precio1;
		this.precio2=precio2;
		this.precio3=precio3;
		this.cantidad1=cantidad1;
		this.cantidad2=cantidad2;
		this.cantidad3=cantidad3;
	}

}